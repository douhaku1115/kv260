/* ================================================================
 * フレームバッファ描画関数
 * ================================================================ */

/* 画面全体を黒でクリア */
static void fb_clear(void)
{
    for (int i = 0; i < HRES * VRES; i++)
        fb[i] = COLOR_BLACK;
    cur_col = 0;
    cur_row = 0;
}

/* 1文字をフレームバッファに描画 */
static void fb_draw_char(char ch, int col, int row)
{
    const u8 *glyph;

    if (ch >= 0x20 && ch <= 0x7E)
        glyph = font_8x16[ch - 0x20];
    else
        glyph = font_8x16[0];

    int base_x = col * CHAR_W;
    int base_y = row * CHAR_H;
    for (int y = 0; y < CHAR_H; y++) {
        u8 bits = glyph[y];
        for (int x = 0; x < CHAR_W; x++) {
            u32 color = (bits & (0x80 >> x)) ? COLOR_GREEN : COLOR_BLACK;
            fb[(base_y + y) * HRES + base_x + x] = color;
        }
    }
}

/* 画面を1文字行分(16ピクセル)上にスクロール */
static void fb_scroll_up(void)
{
    int line_pixels = HRES * CHAR_H;
    for (int i = 0; i < HRES * VRES - line_pixels; i++)
        fb[i] = fb[i + line_pixels];

    for (int i = HRES * VRES - line_pixels; i < HRES * VRES; i++)
        fb[i] = COLOR_BLACK;
}

/* 1文字出力（カーソル管理・スクロール付き） */
static void fb_put_char(char ch)
{
    if (ch == '\n' || ch == '\r') {
        cur_col = 0;
        cur_row++;
    } else if (ch == '\x08' || ch == '\x7f') {
        if (cur_col > 0) {
            cur_col--;
            fb_draw_char(' ', cur_col, cur_row);
        }
    } else {
        fb_draw_char(ch, cur_col, cur_row);
        cur_col++;
    }

    if (cur_col >= COLS) {
        cur_col = 0;
        cur_row++;
    }
    if (cur_row >= ROWS) {
        fb_scroll_up();
        cur_row = ROWS - 1;
    }
}

/* 文字列出力 */
static void fb_put_string(const char *s)
{
    while (*s)
        fb_put_char(*s++);
}

/* ================================================================
 * UART初期化
 * ================================================================ */
static int InitUart(void)
{
    XUartPs_Config *Cfg;
#ifndef SDT
    Cfg = XUartPs_LookupConfig(UART_DEVICE_ID);
#else
    Cfg = XUartPs_LookupConfig(UART_BASEADDR);
#endif
    if (!Cfg) return XST_FAILURE;

    int Status = XUartPs_CfgInitialize(&Uart, Cfg, Cfg->BaseAddress);
    if (Status != XST_SUCCESS) return XST_FAILURE;

    XUartPs_SetBaudRate(&Uart, 115200);
    XUartPs_SetOperMode(&Uart, XUARTPS_OPER_MODE_NORMAL);

    return XST_SUCCESS;
}

/*****************************************************************************/
/**
*
* Main function to call the DPDMA Video example.
*
******************************************************************************/
int main()
{
	int Status;

	Xil_DCacheDisable();
	Xil_ICacheDisable();

	xil_printf("\r\n=== Serial Display (Bare-metal, non-live DP) ===\r\n");

	/* UART1初期化 */
	Status = InitUart();
	if (Status != XST_SUCCESS) {
		xil_printf("UART init failed\r\n");
		return XST_FAILURE;
	}
	xil_printf("UART1 OK (115200 8N1)\r\n");

	/* DisplayPort初期化 + 割り込み設定 */
	Status = DpdmaVideoExample(&RunCfg);
	if (Status != XST_SUCCESS) {
		xil_printf("DPDMA Video Example Failed\r\n");
		return XST_FAILURE;
	}

	xil_printf("DP initialized, waiting for HPD...\r\n");

	/*
	 * HPD割り込みが発生するまで少し待つ。
	 * SetupInterrupts()でHPD割り込みが有効になった時点で
	 * HDMIケーブルが接続されていれば自動的にHPDイベントが発生し、
	 * DpPsu_IsrHpdEvent → DpPsu_Run → リンクトレーニング → 映像出力開始
	 * となる。
	 */
	sleep(2);

	/* HDMIに起動メッセージ表示 */
	fb_put_string("Serial Display Ready (bare-metal)\n");
	fb_put_string("Waiting for UART1 input...\n");
	fb_put_string("---\n");

	xil_printf("Ready. Type in tio to display on HDMI.\r\n");

	/*
	 * メインループ: UART1受信 → HDMI描画
	 * XUartPs_IsReceiveData(): 受信FIFOにデータがあるか確認（ノンブロッキング）
	 * XUartPs_RecvByte(): 1バイト受信
	 */
	while (1) {
		if (XUartPs_IsReceiveData(Uart.Config.BaseAddress)) {
			u8 byte = XUartPs_RecvByte(Uart.Config.BaseAddress);
			char ch = (byte < 128) ? (char)byte : '?';
			fb_put_char(ch);
		}
	}

    return XST_SUCCESS;
}

