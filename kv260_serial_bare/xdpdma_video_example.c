/*
 * 案7ベアメタル版: シリアル受信 → HDMI文字表示
 *
 * 公式DPDMAサンプル(xdpdma_video_example)をベースに、
 * UART1受信 + 8x16フォント描画を追加したもの。
 *
 * ■ システム構成
 *   PC (tio /dev/ttyUSB1) --[UART 115200bps]--> KV260 PS UART1
 *                                                    |
 *                                            DDRフレームバッファ (Frame配列)
 *                                                    |
 *                                            PS DisplayPort (non-live) → HDMI
 *
 * ■ 公式サンプルからの変更点
 *   - GraphicsOverlay(): カラーバー → 黒画面クリア に変更
 *   - UART1初期化 + ポーリング受信ループ追加
 *   - 8x16ビットマップフォント + フレームバッファ描画関数追加
 *   - main()の末尾に無限ループ追加 (UART受信 → HDMI描画)
 *
 * ■ 変更していないファイル
 *   - xdpdma_video_example.h: 型定義、関数プロトタイプ (そのまま)
 *   - xdppsu_interrupt.c: HPDハンドラ、リンクトレーニング (そのまま)
 *
 * ■ 処理の流れ
 *   main()
 *     ├─ キャッシュ無効化
 *     ├─ InitUart()           ← UART1 115200bps初期化
 *     ├─ DpdmaVideoExample()
 *     │    ├─ InitRunConfig()          ← 1920x1080, 5.4Gbps, 2lane
 *     │    ├─ InitDpDmaSubsystem()     ← DP/AVBuf/DPDMA初期化
 *     │    ├─ GraphicsOverlay()        ← 黒画面クリア (変更箇所)
 *     │    ├─ FrameBuffer設定
 *     │    └─ SetupInterrupts()        ← HPD(151) + DPDMA VSYNC(154)
 *     │         ↓ HPDイベント発生
 *     │         DpPsu_IsrHpdEvent → DpPsu_Run → リンクトレーニング → 映像出力開始
 *     │
 *     ├─ 起動メッセージ表示
 *     └─ メインループ: UART受信 → fb_put_char → HDMI表示
 */

